def _build_context(
    goals: list[asyncpg.Record],
    checkin_text: str,
    history: list[asyncpg.Record],
    xp: int,
    new_streak: int,
) -> str:
    parts: list[str] = []

    # Goals
    parts.append("ЦЕЛИ ПОЛЬЗОВАТЕЛЯ:")
    for i, g in enumerate(goals, 1):
        plan_str = ""
        if g["plan"]:
            plan_data = g["plan"] if isinstance(g["plan"], dict) else json.loads(g["plan"])
            items = plan_data.get("items", [])
            if items:
                labels = [item.get("label", "") for item in items]
                plan_str = f" [план: {'; '.join(labels)}]"
        parts.append(f"{i}. {g['goal']}{plan_str}")

    # Check-in
    parts.append(f"\nЧЕК-ИН СЕГОДНЯ:\n{checkin_text}")

    # History
    if history:
        parts.append("\nИСТОРИЯ (последние 5 записей):")
        for entry in history[:5]:
            parts.append(f"{entry['entry_date']}: {entry['checkin_text']}")

    # Gamification
    parts.append(f"\nИГРОВЫЕ ДАННЫЕ:\nxp: {xp}\nxp_gained_today: 100\nstreak_days: {new_streak}")

    return "\n".join(parts)


async def analyze_checkin_manager(
    goals: list[asyncpg.Record],
    checkin_text: str,
    history: list[asyncpg.Record],
    xp: int,
    new_streak: int,
) -> dict[str, Any]:
    context = _build_context(goals, checkin_text, history, xp, new_streak)
    user_message = (
        "Верни ТОЛЬКО валидный JSON по схеме из system prompt.\n"
        "Используй ТОЛЬКО факты из контекста.\n\n"
        f"КОНТЕКСТ:\n{context}"
    )
    return await call_llm(MANAGER_CHECKIN_SYSTEM, user_message, MODEL)
