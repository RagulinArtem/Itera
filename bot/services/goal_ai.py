"""Goal plan generation via LLM."""
from __future__ import annotations

from bot.services.llm_client import call_llm

MODEL = "openai/gpt-4.1-mini"

GOAL_PLAN_SYSTEM = """\
Ты — ассистент по декомпозиции целей для системы Itera.

Твоя задача: превратить цель пользователя в структурированный план действий.

ПРАВИЛА:
- Верни ТОЛЬКО валидный JSON. Без markdown. Без лишнего текста.
- Создай 4–6 конкретных, измеримых шагов.
- Каждый шаг должен быть действием, а не абстракцией.
- Для каждого шага укажи сигнальные слова — ключевые слова, которые помогут системе понять что пользователь работает над этим шагом.
- Тип плана: "sequence" (шаги выполняются по порядку) или "system" (рычаги влияния, для системных целей).

ФОРМАТ ВЫВОДА:
{
  "type": "sequence",
  "items": [
    {
      "id": "step1",
      "kind": "step",
      "label": "Краткое название шага (до 60 символов)",
      "signals": ["ключевое слово1", "ключевое слово2", "..."],
      "definition": "Детальное описание: что именно нужно сделать (1–2 предложения)"
    }
  ]
}

Для системных целей (изменение привычки, долгосрочный проект, процесс):
{
  "type": "system",
  "items": [
    {
      "id": "lever1",
      "kind": "lever",
      "label": "Название рычага влияния",
      "signals": ["сигнал1", "сигнал2"],
      "definition": "Что это рычаг и почему он важен для цели"
    }
  ]
}

Верни ТОЛЬКО JSON."""


async def generate_goal_plan(goal_text: str) -> dict:
    user_message = f"Цель пользователя: {goal_text}\n\nСоздай структурированный план."
    return await call_llm(GOAL_PLAN_SYSTEM, user_message, MODEL)
