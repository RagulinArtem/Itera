def _get_missed_days(last_checkin_date: date | None) -> int:
    if last_checkin_date is None:
        return 0
    delta = (date.today() - last_checkin_date).days
    return max(0, delta - 1)


def _build_context(
    checkin_text: str,
    nickname: str,
    last_checkin_date: date | None,
    goals: list[asyncpg.Record],
    history: list[asyncpg.Record],
    xp: int,
    streak: int,
) -> str:
    missed = _get_missed_days(last_checkin_date)
    parts: list[str] = []

    parts.append(f"DATE: {date.today()}")
    parts.append(f"USER_MESSAGE: {checkin_text}")
    parts.append(f'PROFILE: {{ name: "{nickname}" }}')
    parts.append(
        f"FLAGS: {{ missed_days: {missed}, frustration_hint: false, low_energy_hint: false }}"
    )

    if history:
        parts.append("CHECKINS_RECENT:")
        for entry in history[:3]:
            parts.append(f"{entry['entry_date']}: {entry['checkin_text']}")

    if goals:
        parts.append("GOALS_ACTIVE:")
        for g in goals:
            parts.append(f"- {g['goal']}")

    lcd = str(last_checkin_date) if last_checkin_date else "null"
    parts.append(
        f'METRICS: {{ xp: {xp}, streak: {streak}, last_checkin_date: "{lcd}" }}'
    )

    return "\n".join(parts)


async def analyze_checkin_psychologist(
    checkin_text: str,
    nickname: str,
    last_checkin_date: date | None,
    goals: list[asyncpg.Record],
    history: list[asyncpg.Record],
    xp: int,
    streak: int,
) -> dict:
    context = _build_context(
        checkin_text, nickname, last_checkin_date, goals, history, xp, streak
    )
    return await call_llm(PSYCHOLOGIST_SYSTEM, context, MODEL)
